let grade = 'A';
switch(grade){
    case 'A':
        console.log("You got an A, so here's chocolate for you");
        break;
    case 'B':
        console.log("You got B grade, here's a Cookie for you.");
        break;
    case 'C':
        console.log("You got C, here is your candy ");
        break;
    default:
        console.log("There is no rewards to give");
}